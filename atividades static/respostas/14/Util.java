public class Util {
    /* Método estático que pode ser chamado diretamente pela classe
     sem necessidade de criar uma instância */
    public static void saudacao() {
        System.out.println("Bem-vindo!");
    }
}

/* 
 O código está correto e irá exibir: "Bem-vindo!", pois o método saudacao() é estático,
 então pode ser chamado diretamente pela classe Util, ou seja, não é necessário 
 criar uma instância da classe para usar este método.
 */